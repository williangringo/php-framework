<?php

//------------------------------------------------------------------------------------------------------
// Documentation for routing creation
// http://auraphp.com/packages/Aura.Router/1.1.1/
//------------------------------------------------------------------------------------------------------
$router = container()->get('router');
/* @var $router Aura\Router\Map */

//------------------------------------------------------------------------------------------------------
// Define your custom routes here!
//------------------------------------------------------------------------------------------------------



//------------------------------------------------------------------------------------------------------
// Asset route (for URL generation only)
//------------------------------------------------------------------------------------------------------
$router->add('asset', '/assets/{:f}', [
    'method'   => ['GET'],
    'routable' => false
]);

//------------------------------------------------------------------------------------------------------
// Module route
//------------------------------------------------------------------------------------------------------
$router->add('m', '/{:m}', [
    'params' => [
        'm' => '([a-zA-Z0-9\-_]+)'
    ],
    'values' => [
        'm' => 'base'
    ],
    'method' => ['GET', 'POST']
]);

//------------------------------------------------------------------------------------------------------
// Module / Controller route
//------------------------------------------------------------------------------------------------------
$router->add('mc', '/{:m}/{:c}', [
    'params' => [
        'm' => '([a-zA-Z0-9\-_]+)',
        'c' => '([a-zA-Z0-9\-_]+)'
    ],
    'values' => [
        'm' => 'base',
        'c' => 'index'
    ],
    'method' => ['GET', 'POST']
]);

//------------------------------------------------------------------------------------------------------
// Module / Controller / Action route
//------------------------------------------------------------------------------------------------------
$router->add('mca', '/{:m}/{:c}/{:a}', [
    'params' => [
        'm' => '([a-zA-Z0-9\-_]+)',
        'c' => '([a-zA-Z0-9\-_]+)',
        'a' => '([a-zA-Z0-9\-_]+)'
    ],
    'values' => [
        'm' => 'base',
        'c' => 'index',
        'a' => 'index'
    ],
    'method' => ['GET', 'POST']
]);

//------------------------------------------------------------------------------------------------------
// Module / Controller / Action / ID route
//------------------------------------------------------------------------------------------------------
$router->add('mcaid', '/{:m}/{:c}/{:a}/{:id}', [
    'params' => [
        'm'  => '([a-zA-Z0-9\-_]+)',
        'c'  => '([a-zA-Z0-9\-_]+)',
        'a'  => '([a-zA-Z0-9\-_]+)',
        'id' => '([a-zA-Z0-9\-_]+)'
    ],
    'values' => [
        'm'  => 'base',
        'c'  => 'index',
        'a'  => 'index',
        'id' => -1
    ],
    'method' => ['GET', 'POST']
]);


